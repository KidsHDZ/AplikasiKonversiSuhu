    private void konversiSuhu() {
        try {
            double suhuInput = Double.parseDouble(jTextField1.getText());
            String skalaAwal = jComboBox1.getSelectedItem().toString();
            String skalaTujuan = jComboBox2.getSelectedItem().toString();
            double hasilKonversi = 0;

            if (jRadioButton1.isSelected()) { // Kiri ke Kanan
                hasilKonversi = hitungKonversi(suhuInput, skalaAwal, skalaTujuan);
            } else { // Kanan ke Kiri
                hasilKonversi = hitungKonversi(suhuInput, skalaTujuan, skalaAwal);
            }

            JOptionPane.showMessageDialog(this, "Hasil konversi: " + hasilKonversi);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input tidak valid!");
        }
    }
    private double hitungKonversi(double suhu, String dari, String ke) {
        switch (dari + "->" + ke) {
            case "Celcius->Fahrenheit":
                return (suhu * 9/5) + 32;
            case "Fahrenheit->Celcius":
                return (suhu - 32) * 5/9;
            case "Celcius->Kelvin":
                return suhu + 273.15;
            case "Kelvin->Celcius":
                return suhu - 273.15;
            case "Celcius->Reamur":
                return suhu * 4/5;
            case "Reamur->Celcius":
                return suhu * 5/4;
            case "Fahrenheit->Kelvin":
                return (suhu - 32) * 5/9 + 273.15;
            case "Kelvin->Fahrenheit":
                return (suhu - 273.15) * 9/5 + 32;
            // Tambahkan rumus konversi lainnya jika diperlukan
            default:
                return suhu; // Jika tidak ada perubahan
        }
    }
    
